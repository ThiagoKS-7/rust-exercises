pub mod expressions {
    pub fn add_numbers(a: f32, b: f32) -> f32 {
        return a + b;
    }
    pub fn subtract_numbers(a: f32, b: f32) -> f32 {
        return a - b;
    }
    pub fn multiply_numbers(a: f32, b: f32) -> f32 {
        return a * b;
    } // ela nÃ£o precisa dar return
    pub fn divide_numbers(a: f32, b: f32) -> f32 {
        return a / b;
    } // ela nÃ£o precisa dar return
}
